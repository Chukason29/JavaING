public class Variables {
    public static void main (String args[]){
        byte myAge = 45;
        short allChurches = 2344;
        long humanPopulation = 14443194;
        String myName = "Chukwuebuka";
        boolean isMarried = false;
        char initials = 'V';
        float accountBal = 5.35f;// the 'f' tells the compiler that it is float variable
        double longDecimals = 29187.25d;

        System.out.println("My age is "+ myAge);
        System.out.println("My name is " + myName);
        System.out.println(isMarried);
        System.out.println(initials);
        System.out.println(accountBal);
        System.out.println(allChurches);
        System.out.println(humanPopulation);
        System.out.println(longDecimals);       
    }
}